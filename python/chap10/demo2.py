#个数可变的位置形参
def fun(*args): #结果为元组
    print(args)

fun(10,20)
fun(10)
fun(90,38,93)
fun(34,99,88)
#个数可变的关键字形参
def fun1(**args): #返回为字典
    print(args)

fun1(a=10)
fun1(a=10,b=20,c=30)
'''可变位置参数只能有一个'''

'''
在函数的定义过程中，既有个数可变的关键字形参，也有个数可变的位置形参，
要求，个数可变的位置形参，放在个数可变的关键字形参之前
'''

